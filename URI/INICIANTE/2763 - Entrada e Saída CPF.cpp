#include <bits/stdc++.h>

using namespace std;

int main()
{

    string s;
    
    while(cin >> s)
    {
        cout << s[0] << s[1] << s[2] << endl;
        cout << s[4] << s[5] << s[6] << endl;
        cout << s[8] << s[9] << s[10] << endl;
        cout << s[12] << s[13] << endl;
    }

    cerr << "Time elapsed: " << 1.0 * clock() / CLOCKS_PER_SEC << " s.\n";
    return 0;
}
